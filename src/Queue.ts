/******************************************************************************************************************************
    
    This file creates the generic class Queue<T> which is a first-in-first-out type of queue

*******************************************************************************************************************************/

export class Queue<T> {

    private queue : Array<T>;

    /******************************************************************************************************************************
        Constructor that initializes an empty queue
    *******************************************************************************************************************************/
    constructor() {
        this.queue = [];
    }

    /******************************************************************************************************************************
        Method that adds an element to the queue
    *******************************************************************************************************************************/
    public add = (t : T) : void => {
        this.queue.push(t);
    }

    /******************************************************************************************************************************
        Method that removes the first inserted element from the queue
    *******************************************************************************************************************************/
    public remove = () : T|null => {
        const t : T | undefined = this.queue.shift();
        if (t) {
            return t;
        }
        else {
            return null;
        }
    }

    /******************************************************************************************************************************
        Method that returns the array representing the queue
    *******************************************************************************************************************************/
    public getQueue = () : Array<T> => {
        return this.queue;
    }

    /******************************************************************************************************************************
        Method that returns the length of the queue
    *******************************************************************************************************************************/
    public getLength = () : number => {
        return this.queue.length;
    }

}